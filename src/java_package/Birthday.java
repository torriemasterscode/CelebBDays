package java_package;

public class Birthday {
	private String month;
	private int date;
	
	public Birthday(String month, int date) {
		super();
		this.month = month;
		this.date = date;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public int getDate() {
		return date;
	}

	public void setDate(int date) {
		this.date = date;
	}

}
